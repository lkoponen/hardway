puts "The '+' operator in Ruby is used for addition"
puts "Ex. 2 + 4 = ", 2 + 4
puts "Ex. 7 + 12 = ", 7 + 12
puts

puts "The '-' operator is used for subtraction"
puts "Ex. 4 - 2 = ", 4 - 2
puts "Ex. 6 - 3 = ", 6 - 3
puts

puts "The '/' operator is used for division"
puts "Ex. 4 / 2 = ", 4 / 2
puts "Ex. 8 / 4 = ", 8 / 4
puts <<MESSAGE
NOTE: Dividing an integer by an integer will return an integer(ie. no decimals)
If you want decimals, you must use floats.
MESSAGE

puts "Ex. 7.0/2.0 = ", 7.0/2.0
puts "whereas 7 / 2 = ", 7 / 2
puts

puts "The '*' operator is used for multiplication"
puts "Ex. 8 * 4 = ", 8 * 4
puts "Ex. 16 * 2.5 = ", 16 * 2.5

